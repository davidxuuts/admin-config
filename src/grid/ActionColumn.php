    public function init()
    {
        parent::init();
        $this->initDefaultButtons();
    }

    /**
     * Initializes the default button rendering callbacks.
     */
    protected function initDefaultButtons()
    {
        $this->initDefaultButton('view', 'view');
        $this->initDefaultButton('create', 'create');
        $this->initDefaultButton('update', 'update');
        $this->initDefaultButton('edit', 'edit');
        $this->initDefaultButton('ajax-edit', 'ajax-edit', [
            'data-toggle' => 'modal',
            'data-target' => '#modal',
            'title' => Yii::t('yii', 'Edit'),
            'aria-label' => Yii::t('yii', 'Edit'),
            'data-modal-class' => ModalSizeEnum::SIZE_LARGE,
        ]);
        $this->initDefaultButton('destroy', 'destroy', [
            'data-confirm' => Yii::t('yii', 'Are you sure you want to delete this item?'),
            'data-method' => 'post',
        ]);
        $this->initDefaultButton('delete', 'delete', [
            'data-confirm' => Yii::t('yii', 'Are you sure you want to delete this item?'),
            'data-method' => 'post',
        ]);
    }

    /**
     * Initializes the default button rendering callback for single button.
     * @param string $name Button name as it's written in template
     * @param string $iconName The part of Bootstrap glyphicon class that makes it unique
     * @param array $additionalOptions Array of additional options
     * @since 2.0.11
     */
    protected function initDefaultButton($name, $iconName, $additionalOptions = [])
    {
        if (!isset($this->buttons[$name]) && str_contains($this->template, '{' . $name . '}')) {
            $this->buttons[$name] = function ($url) use ($name, $iconName, $additionalOptions) {
                $title = match ($name) {
                    'view' => Yii::t('yii', 'View'),
                    'create' => Yii::t('yii', 'Create'),
                    'update' => Yii::t('yii', 'Update'),
                    'edit' => Yii::t('yii', 'Edit'),
                    'destroy' => Yii::t('yii', 'Destroy'),
                    'delete' => Yii::t('yii', 'Delete'),
                    default => ucfirst($name),
                };
                $options = array_merge([
                    'title' => $title,
                    'aria-label' => $title,
                    'data-pjax' => '0',
                ], $additionalOptions, $this->buttonOptions);
                $icon = $this->icons[$iconName] ?? Html::tag('i', '', ['class' => "fas fa-$iconName"]);
                return Html::a($icon, $url, $options);
            };
        }
    }
